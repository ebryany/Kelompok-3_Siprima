    private boolean validateBasicForm() {
        StringBuilder errors = new StringBuilder();
        
        if (isFieldEmpty(txtNamaLengkap)) {
            errors.append("‚Ä¢ Nama lengkap harus diisi\n");
        }
        
        if (isFieldEmpty(txtNIK)) {
            errors.append("‚Ä¢ NIK harus diisi\n");
} else if (!txtNIK.getText().matches("^\\d{16}$")) {
            errors.append("‚Ä¢ NIK harus 16 digit angka\n");
        }
        
        if (isFieldEmpty(txtEmail)) {
            errors.append("‚Ä¢ Email harus diisi\n");
        } else if (!isValidEmail(txtEmail.getText())) {
            errors.append("‚Ä¢ Format email tidak valid\n");
        }
        
        if (errors.length() > 0) {
            JOptionPane.showMessageDialog(this,
                "‚ö†Ô∏è Mohon perbaiki kesalahan berikut:\n\n" + errors.toString(),
                "Validasi Error",
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    private boolean validateCompleteForm() {
        if (!validateBasicForm()) {
            return false;
        }
        
        StringBuilder errors = new StringBuilder();
        
        if (isFieldEmpty(txtAlamat)) {
            errors.append("‚Ä¢ Alamat lengkap harus diisi\n");
        }
        
        if (cmbKategori.getSelectedItem() == null || cmbKategori.getSelectedIndex() <= 0) {
            errors.append("‚Ä¢ Kategori aduan harus dipilih\n");
        }
        
        if (isFieldEmpty(txtLokasiKejadian)) {
            errors.append("‚Ä¢ Lokasi kejadian harus diisi\n");
        }
        
        if (isFieldEmpty(txtJudulAduan)) {
            errors.append("‚Ä¢ Judul aduan harus diisi\n");
        }
        
        if (txtDeskripsi.getText().trim().isEmpty() || 
            txtDeskripsi.getText().equals("Deskripsi lengkap aduan Anda...")) {
            errors.append("‚Ä¢ Deskripsi aduan harus diisi\n");
        }
        
        if (errors.length() > 0) {
            JOptionPane.showMessageDialog(this,
                "‚ö†Ô∏è Mohon lengkapi data berikut:\n\n" + errors.toString(),
                "Validasi Error",
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    private boolean isFieldEmpty(JTextField field) {
        return field.getText().trim().isEmpty() || isPlaceholderText(field.getText());
    }
    
    private boolean isValidEmail(String email) {
        return email.matches("^[A-Za-z0-9+_.-]+@(.+)$");
    }
    
