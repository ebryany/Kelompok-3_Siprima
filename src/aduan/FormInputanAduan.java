                // Change color based on progress
                if (progress < 30) {
                    formProgressBar.setForeground(ERROR_COLOR);
                } else if (progress < 70) {
                    formProgressBar.setForeground(WARNING_COLOR);
                } else {
                    formProgressBar.setForeground(SUCCESS_COLOR);
                }
